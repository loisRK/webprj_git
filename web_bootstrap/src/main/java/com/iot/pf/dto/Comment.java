package com.iot.pf.dto;

import java.sql.Timestamp;

import org.apache.ibatis.type.Alias;

@Alias("Comment")
public class Comment {
	private int comNum;
	private String comDocType;
	private int comDocNum;
	private String userId;
	private String comContents;
	private Timestamp comDate;
	
	public int getComNum() {
		return comNum;
	}
	public void setComNum(int comNum) {
		this.comNum = comNum;
	}
	public String getComDocType() {
		return comDocType;
	}
	public void setComDocType(String comDocType) {
		this.comDocType = comDocType;
	}
	public int getComDocNum() {
		return comDocNum;
	}
	public void setComDocNum(int comDocNum) {
		this.comDocNum = comDocNum;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getComContents() {
		return comContents;
	}
	public void setComContents(String comContents) {
		this.comContents = comContents;
	}
	public Timestamp getComDate() {
		return comDate;
	}
	public void setComDate(Timestamp comDate) {
		this.comDate = comDate;
	}
	
	@Override
	public String toString() {
		return "Comment [comNum=" + comNum + ", comDocType=" + comDocType + ", comDocNum=" + comDocNum + ", userId="
				+ userId + ", comContents=" + comContents + ", comDate=" + comDate + "]";
	}
	
}
